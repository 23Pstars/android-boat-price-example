package id.web.zaf.boatprice;

public class BoatPrice {
    private String fetchDate, price, depart, departTime, arrive, arriveTime, agent, boat, agentSite, boatLogo, boatPhoto;

    public String getFetchDate() {
        return fetchDate;
    }

    public void setFetchDate(String fetchDate) {
        this.fetchDate = fetchDate;
    }

    public String getPrice() {
        return price;
    }

    public void setPrice(String price) {
        this.price = price;
    }

    public String getDepart() {
        return depart;
    }

    public void setDepart(String depart) {
        this.depart = depart;
    }

    public String getDepartTime() {
        return departTime;
    }

    public void setDepartTime(String departTime) {
        this.departTime = departTime;
    }

    public String getArrive() {
        return arrive;
    }

    public void setArrive(String arrive) {
        this.arrive = arrive;
    }

    public String getArriveTime() {
        return arriveTime;
    }

    public void setArriveTime(String arriveTime) {
        this.arriveTime = arriveTime;
    }

    public String getAgent() {
        return agent;
    }

    public void setAgent(String agent) {
        this.agent = agent;
    }

    public String getBoat() {
        return boat;
    }

    public void setBoat(String boat) {
        this.boat = boat;
    }

    public String getAgentSite() {
        return agentSite;
    }

    public void setAgentSite(String agentSite) {
        this.agentSite = agentSite;
    }

    public String getBoatLogo() {
        return boatLogo;
    }

    public void setBoatLogo(String boatLogo) {
        this.boatLogo = boatLogo;
    }

    public String getBoatPhoto() {
        return boatPhoto;
    }

    public void setBoatPhoto(String boatPhoto) {
        this.boatPhoto = boatPhoto;
    }
}
